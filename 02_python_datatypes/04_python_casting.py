# integer
x = int(1)
y = int(2.8)
z = int("3")
print("-------Integer--------")
print(x, type(x))
print(y, type(y))
print(z, type(z))

# float
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print("-------float--------")
print(x, type(x))
print(y, type(y))
print(z, type(z))
print(w, type(z))

# string

x = str("ronald")
y = str("2")
z = str("3.0")
print("-------String--------")
print(x, type(x))
print(y, type(y))
print(z, type(z))
