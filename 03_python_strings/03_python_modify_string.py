# Upper case
a = "Hello, World!"
print(a.upper())

# lowercase
a = "HelloWorld!"
b = a.lower()
print(b)

# remove white spaces
a = "Hello World!  "
print(a.strip())

# replace string
a = "Hello, World!"
print(a.replace("Hello", "Bye"))

# split string
a = "Hello, World , going , to end"
print(a.split(","))
